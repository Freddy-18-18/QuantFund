#[cfg(test)]
mod tests {
    use super::*;
    use quantfund_core::{Price, TickEvent, Volume};
    use rust_decimal_macros::dec;
    use std::io::Write;
    use tempfile::NamedTempFile;

    fn make_tick(instrument: &str, ts_nanos: i64) -> TickEvent {
        TickEvent {
            timestamp: Timestamp::from_nanos(ts_nanos),
            instrument_id: InstrumentId::new(instrument),
            bid: Price::new(dec!(1.1000)),
            ask: Price::new(dec!(1.1002)),
            bid_volume: Volume::new(dec!(100)),
            ask_volume: Volume::new(dec!(100)),
            spread: dec!(0.0002),
        }
    }

    fn make_snapshot<'a>(tick: &'a TickEvent, id: &'a InstrumentId) -> MarketSnapshot<'a> {
        MarketSnapshot {
            tick,
            instrument_id: id,
        }
    }

    fn signal_json(ts_nanos: i64, side: &str, strength: f64) -> String {
        format!(
            r#"[{{"timestamp":{{"nanos_since_epoch":{ts_nanos}}},"strategy_id":"py","instrument_id":"EURUSD","side":{side},"strength":{strength},"metadata":{{}}}}]"#
        )
    }

    #[test]
    fn no_signal_when_file_missing() {
        let config = PythonSignalRelayConfig {
            signals_path: PathBuf::from("/nonexistent/path/signals.json"),
            poll_interval_ms: 0,
            instruments: vec![InstrumentId::new("EURUSD")],
            ..Default::default()
        };
        let mut relay = PythonSignalRelay::new(config);
        let id = InstrumentId::new("EURUSD");
        let tick = make_tick("EURUSD", 1_000_000_000);
        let snapshot = make_snapshot(&tick, &id);

        // Missing file → no signal (flat returns None)
        assert!(relay.on_tick(&snapshot).is_none());
    }

    #[test]
    fn emits_signal_from_json_file() {
        let ts_nanos: i64 = 1_700_000_000_000_000_000; // well within threshold
        let mut tmp = NamedTempFile::new().unwrap();
        let json = signal_json(ts_nanos, r#""Buy""#, 0.75);
        tmp.write_all(json.as_bytes()).unwrap();
        tmp.flush().unwrap();

        let config = PythonSignalRelayConfig {
            signals_path: tmp.path().to_path_buf(),
            poll_interval_ms: 0,
            stale_threshold_secs: 3600,
            instruments: vec![InstrumentId::new("EURUSD")],
            strategy_id: "py".to_string(),
            ..Default::default()
        };
        let mut relay = PythonSignalRelay::new(config);
        let id = InstrumentId::new("EURUSD");
        let tick = make_tick("EURUSD", ts_nanos + 1_000_000_000); // 1s after signal
        let snapshot = make_snapshot(&tick, &id);

        let signal = relay.on_tick(&snapshot).expect("expected a signal");
        assert_eq!(signal.side, Some(Side::Buy));
        assert!((signal.strength - 0.75).abs() < 1e-9);
    }

    #[test]
    fn stale_signal_returns_none() {
        let old_ts_nanos: i64 = 1_000_000_000; // epoch + 1s
        let mut tmp = NamedTempFile::new().unwrap();
        let json = signal_json(old_ts_nanos, r#""Buy""#, 0.5);
        tmp.write_all(json.as_bytes()).unwrap();
        tmp.flush().unwrap();

        let now_ns: i64 = old_ts_nanos + 120 * 1_000_000_000; // 2 minutes later
        let config = PythonSignalRelayConfig {
            signals_path: tmp.path().to_path_buf(),
            poll_interval_ms: 0,
            stale_threshold_secs: 60,
            instruments: vec![InstrumentId::new("EURUSD")],
            ..Default::default()
        };
        let mut relay = PythonSignalRelay::new(config);
        let id = InstrumentId::new("EURUSD");
        let tick = make_tick("EURUSD", now_ns);
        let snapshot = make_snapshot(&tick, &id);

        // Stale → flat → on_tick returns None
        assert!(relay.on_tick(&snapshot).is_none());
    }

    #[test]
    fn clamps_strength_to_unit_interval() {
        let ts_nanos: i64 = 1_700_000_000_000_000_000;
        let mut tmp = NamedTempFile::new().unwrap();
        // strength = 5.0 → should be clamped to 1.0
        let json = signal_json(ts_nanos, r#""Buy""#, 5.0);
        tmp.write_all(json.as_bytes()).unwrap();
        tmp.flush().unwrap();

        let config = PythonSignalRelayConfig {
            signals_path: tmp.path().to_path_buf(),
            poll_interval_ms: 0,
            stale_threshold_secs: 3600,
            instruments: vec![InstrumentId::new("EURUSD")],
            strategy_id: "py".to_string(),
            ..Default::default()
        };
        let mut relay = PythonSignalRelay::new(config);
        let id = InstrumentId::new("EURUSD");
        let tick = make_tick("EURUSD", ts_nanos + 1_000_000_000);
        let snapshot = make_snapshot(&tick, &id);

        let signal = relay.on_tick(&snapshot).expect("expected a signal");
        assert!(
            (signal.strength - 1.0).abs() < 1e-9,
            "strength={}",
            signal.strength
        );
    }

    #[test]
    fn reset_clears_cached_signals() {
        let ts_nanos: i64 = 1_700_000_000_000_000_000;
        let mut tmp = NamedTempFile::new().unwrap();
        let json = signal_json(ts_nanos, r#""Sell""#, -0.6);
        tmp.write_all(json.as_bytes()).unwrap();
        tmp.flush().unwrap();

        let config = PythonSignalRelayConfig {
            signals_path: tmp.path().to_path_buf(),
            poll_interval_ms: 0,
            stale_threshold_secs: 3600,
            instruments: vec![InstrumentId::new("EURUSD")],
            strategy_id: "py".to_string(),
            ..Default::default()
        };
        let mut relay = PythonSignalRelay::new(config);
        let id = InstrumentId::new("EURUSD");
        let tick = make_tick("EURUSD", ts_nanos + 1_000_000_000);
        let snapshot = make_snapshot(&tick, &id);

        // First tick: populates cache
        relay.on_tick(&snapshot).expect("expected a signal");
        assert!(!relay.cached.is_empty());

        // Reset: clears cache and last_poll
        relay.reset();
        assert!(relay.cached.is_empty());
        assert!(relay.last_poll.is_none());
    }
}
